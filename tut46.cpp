// !----Constructors in Derived Class in C++----!

/*

In C++, constructors in a derived class are used to initialize both the derived class's own members and the inherited members from the base class. When an object of the derived class is created, the constructor of the base class is automatically called before the derived class constructor. This ensures proper initialization of both base and derived class parts of the object. To initialize the base class part, you specify the base class constructor in the derived class constructor's member initializer list.

*/